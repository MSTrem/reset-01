public class Calculadora {
    int somar(int parametroUm, int parametroDois) {
        return parametroUm + parametroDois;
    }

    int subtrai(int parametroUm, int parametroDois) {
        return parametroUm - parametroDois;
    }

    int multiplique(int parametroUm, int parametroDois) {
        return parametroUm * parametroDois;
    }

    double divide(double parametroUm, double parametroDois) {
        return parametroUm / parametroDois;
    }
}